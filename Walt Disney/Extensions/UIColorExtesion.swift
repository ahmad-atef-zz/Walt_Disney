import Foundation

extension UIColor {
    static var movieTitleBigColor: UIColor {
        get {
            return .black
        }
    }
}

extension UIFont {
    static var primaryFont: UIFont {
        get {
            return UIFont.boldSystemFont(ofSize: 20.0)
        }
    }
}
